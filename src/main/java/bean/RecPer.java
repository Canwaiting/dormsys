package bean;

public class RecPer {
    String rec_id;
    String per_name;
    String per_tel;
    String rec_per_sta;
    String rec_per_time;

    public String getRec_id() {
        return rec_id;
    }

    public void setRec_id(String rec_id) {
        this.rec_id = rec_id;
    }

    public String getPer_name() {
        return per_name;
    }

    public void setPer_name(String per_name) {
        this.per_name = per_name;
    }

    public String getPer_tel() {
        return per_tel;
    }

    public void setPer_tel(String per_tel) {
        this.per_tel = per_tel;
    }

    public String getRec_per_sta() {
        return rec_per_sta;
    }

    public void setRec_per_sta(String rec_per_sta) {
        this.rec_per_sta = rec_per_sta;
    }

    public String getRec_per_time() {
        return rec_per_time;
    }

    public void setRec_per_time(String rec_per_time) {
        this.rec_per_time = rec_per_time;
    }
}

package bean;

public class Bldg {
    String bldg_id;
    String bldg_floor;
    String bldg_pos;

    public String getBldg_id() {
        return bldg_id;
    }

    public void setBldg_id(String bldg_id) {
        this.bldg_id = bldg_id;
    }

    public String getBldg_floor() {
        return bldg_floor;
    }

    public void setBldg_floor(String bldg_floor) {
        this.bldg_floor = bldg_floor;
    }

    public String getBldg_pos() {
        return bldg_pos;
    }

    public void setBldg_pos(String bldg_pos) {
        this.bldg_pos = bldg_pos;
    }
}

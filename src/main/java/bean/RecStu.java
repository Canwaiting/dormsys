package bean;

public class RecStu {
    String rec_id;
    String stu_id;
    String stu_name;
    String rec_stu_sta;
    String rec_stu_time;

    public String getRec_id() {
        return rec_id;
    }

    public void setRec_id(String rec_id) {
        this.rec_id = rec_id;
    }

    public String getStu_id() {
        return stu_id;
    }

    public void setStu_id(String stu_id) {
        this.stu_id = stu_id;
    }

    public String getStu_name() {
        return stu_name;
    }

    public void setStu_name(String stu_name) {
        this.stu_name = stu_name;
    }

    public String getRec_stu_sta() {
        return rec_stu_sta;
    }

    public void setRec_stu_sta(String rec_stu_sta) {
        this.rec_stu_sta = rec_stu_sta;
    }

    public String getRec_stu_time() {
        return rec_stu_time;
    }

    public void setRec_stu_time(String rec_stu_time) {
        this.rec_stu_time = rec_stu_time;
    }
}

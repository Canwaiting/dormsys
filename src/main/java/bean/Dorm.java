package bean;

public class Dorm {
    String dorm_id;
    String dorm_size;
    String bldg_id;

    public String getDorm_id() {
        return dorm_id;
    }

    public void setDorm_id(String dorm_id) {
        this.dorm_id = dorm_id;
    }

    public String getDorm_size() {
        return dorm_size;
    }

    public void setDorm_size(String dorm_size) {
        this.dorm_size = dorm_size;
    }

    public String getBldg_id() {
        return bldg_id;
    }

    public void setBldg_id(String bldg_id) {
        this.bldg_id = bldg_id;
    }
}
